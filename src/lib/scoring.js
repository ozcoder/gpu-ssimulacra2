export function computeScore(subscores, numScales) {
  let raw = 0;
  let wi = 0;
  for (let c = 0; c < 3; c++) {
    for (let s = 0; s < numScales; s++) {
      const off = s * 18;
      for (let n = 0; n < 2; n++) {
        raw += WEIGHTS[wi++] * Math.abs(subscores[off + c * 2 + n]);
        raw += WEIGHTS[wi++] * Math.abs(subscores[off + 6 + c * 4 + n]);
        raw += WEIGHTS[wi++] * Math.abs(subscores[off + 6 + c * 4 + n + 2]);
      }
    }
  }

  raw *= 0.9562382616834844;
  raw = 2.326765642916932 * raw - 0.020884521182843837 * raw * raw +
        6.248496625763138e-05 * raw * raw * raw;

  return raw > 0
    ? 100.0 - 10.0 * Math.pow(raw, 0.6276336467831387)
    : 100.0;
}
